function makeCandleData(dataToChange) {
    // console.log('dataToChange: ', dataToChange[0]);
    // let data
    return dataToChange.reduce((acc, candle) => {
        const candleToPush = {
            close: +candle[4],
            high: +candle[2],
            low: +candle[3],
            open: +candle[1],
            timestamp: +candle[0],
            volume: +candle[5]
        }
        // close: candle[4]
        // high: candle[2]
        // low: candle[3]
        // open:candle[1]
        // timestamp: candle[0]
        // timestamp: candle[6]?
        // volume: candle[5]
        acc.push(candleToPush)

        return acc
    }, [])

}

